public class Palabra {
    private String palabra;
    
    
    public Palabra(){
    }
    
    public Palabra(String unaPalabra){
        palabra = unaPalabra;
    }
    
    public String getPalabra() {
        return palabra;
    }
    
    public void setPalabra(String unaPalabra) {
        palabra = unaPalabra;
    }
    
}